package com.rightkarma.learnjava.javabasic.misc;

public class FloatingPoint {
    public static void main(String[] args) {
        System.out.println("---float---");
        System.out.println(Float.POSITIVE_INFINITY);// LearningNote - Infinity
        System.out.println(Float.NEGATIVE_INFINITY);// LearningNote - -Infinity
        System.out.println(Float.NaN); // LearningNote - NaN
        System.out.println(Float.MAX_VALUE);// LearningNote - 3.4028235E38
        System.out.println(Float.MIN_VALUE);// LearningNote - 1.4E-45

        System.out.println("---double---");
        System.out.println(Double.POSITIVE_INFINITY);// LearningNote - Infinity
        System.out.println(Double.NEGATIVE_INFINITY);// LearningNote - -Infinity
        System.out.println(Double.NaN);// LearningNote - NaN
        System.out.println(Double.MAX_VALUE);// LearningNote - 1.7976931348623157E308
        System.out.println(Double.MIN_VALUE);// LearningNote - 4.9E-324

    }
}
